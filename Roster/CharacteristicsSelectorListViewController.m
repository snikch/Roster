@implementation CharacteristicsSelectorListViewController

- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    if ([[segue identifier] isEqualToString:@"editCharacteristics"]) {
        CharacteristicsSelectorEditViewController *vc = (CharacteristicsSelectorEditViewController *)[segue destinationViewController];
        vc.presentingPopoverController = self.presentingPopoverController;
        NSIndexPath *indexPath = [self.tableView indexPathForSelectedRow];
        NSManagedObject *object = [self.fetchedResultsController objectAtIndexPath:indexPath];
        NSLog(@"Editing chars with object %@ in IP %@", object, indexPath);
        vc.type = [object valueForKey:@"type"];
        vc.managedObjectContext = self.managedObjectContext;
        vc.delegate = self.delegate;
    }
    
}

-(void)viewWillDisappear:(BOOL)animated{
    _fetchedResultsController = nil;
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return [[self.fetchedResultsController sections] count];
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    id <NSFetchedResultsSectionInfo> sectionInfo = [self.fetchedResultsController sections][section];
    NSLog(@"%d rows in section", [sectionInfo numberOfObjects]);
    return [sectionInfo numberOfObjects];
}

